"""
Calibration reference implementation matching contracts/src/ScoringEngine.sol §7.4.2.

Storage layout (per agent per category):
    bucket_accuracy[10] : int256, 1e6-scaled fractional accuracy ∈ [0, 1e6]
    bucket_count[10]    : uint256, raw counts

Per-call update (on each resolution):
    bucket_idx = confidence_bps // 1000                          (∈ [0, 9])
    realized_scaled = (score + 1_000_000) // 2                   (∈ [0, 1e6])
    bucket_accuracy[bucket_idx]
        = (9 * bucket_accuracy[bucket_idx] + realized_scaled) // 10
    bucket_count[bucket_idx] += 1

Calibration on read:
    total_count = Σ bucket_count[i]
    if total_count < 10: return 0   # cold start; UI shows "calibrating"
    For each i in 0..9:
        midpoint_scaled = i * 100_000 + 50_000     (∈ [50_000, 950_000])
        diff           = midpoint_scaled - bucket_accuracy[i]
        sq             = diff * diff               (∈ [0, ~1e12])
        sum_weighted_sq += sq * bucket_count[i]
    cal_score = -((sum_weighted_sq * 4) // (total_count * 1_000_000))
    clamp to [-1_000_000, 0]

Run:
    python calibration_reference.py
"""

SCALE = 1_000_000
ALPHA_NUM = 1
ALPHA_DEN = 10
CAL_MIN = -1_000_000
CAL_MAX = 0


def realized_scaled(score: int) -> int:
    """Map signed score ∈ [-1e6, 1e6] to scaled realized accuracy ∈ [0, 1e6]."""
    return (score + SCALE) // 2


def ema_update(old_scaled: int, realized: int) -> int:
    """EMA with α = 0.1 in 1e6-scaled integer space: new = (9*old + realized) // 10."""
    return ((ALPHA_DEN - ALPHA_NUM) * old_scaled + ALPHA_NUM * realized) // ALPHA_DEN


def calibration_score(bucket_accuracy, bucket_count) -> int:
    """Compute calibration score from current bucket state. Both inputs length-10."""
    assert len(bucket_accuracy) == 10 and len(bucket_count) == 10
    total = sum(bucket_count)
    if total < 10:
        return 0
    sum_weighted_sq = 0
    for i in range(10):
        midpoint = i * 100_000 + 50_000
        diff = midpoint - bucket_accuracy[i]
        sq = diff * diff
        sum_weighted_sq += sq * bucket_count[i]
    cal = -((sum_weighted_sq * 4) // (total * SCALE))
    if cal < CAL_MIN:
        cal = CAL_MIN
    if cal > CAL_MAX:
        cal = CAL_MAX
    return cal


